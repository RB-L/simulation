function xdot = myddesd(t,x,Z)

k1=10;k2=10;k3=10;a1=1;a2=1;a3=20;kar=0.5;
D=1;c1=100;c2=200;c3=200;rou=2;w1=0.2;w2=0.01;b1=0.1;b2=0.2;f1=0;f2=0;f3=0;


yd=0.2*(sin(t)+sin(0.8*t));  
dyd=0.2*(cos(t)+0.8*cos(0.8*t));
ddyd=0.2*(-sin(t)-0.64*sin(0.8*t));
dddyd=0.2*(-cos(t)-0.512*cos(0.8*t));


z1=x(1)-yd;
z2=x(5)-x(7);
z3=x(6)-x(8);

%%
for i=1:80;
   s1(i)=exp(-((yd-5+0.125*i)^2+(dyd-5+0.125*i)^2+(x(1)-5+0.125*i)^2+(x(9)-5+0.125*i)^2)/4);
end


for i=1:80;
    f1=f1+s1(i)^2;
end
        
m1=(81*(f1+1))^0.5;

sig1=-(c1*z1)-(z1^3*x(9)*m1^2)/(2*a1^2);
%%


for ii=1:80;
   s2(ii)=exp(-((yd-5+0.125*ii)^2+(dyd-5+0.125*ii)^2+(ddyd-5+0.125*ii)^2+(x(1)-5+0.125*ii)^2+(x(4)-5+0.125*ii)^2+(x(5)-5+0.125*ii)^2+(x(9)-5+0.125*ii)^2)/4); 
end

for ii=1:80;
    f2=f2+s2(ii)^2;
end

m2=(81*(f2+1))^0.5;

sig2=(-c2*z2)-(z2^3*x(9)*m2^2)/(2*a2^2);
%%


for iii=1:80;
   s3(iii)=exp(-((yd-5+0.125*iii)^2+(dyd-5+0.125*iii)^2+(ddyd-5+0.125*iii)^2+(dddyd-5+0.125*iii)^2+(x(1)-5+0.125*iii)^2+(x(4)-5+0.125*iii)^2+(x(5)-5+0.125*iii)^2+(x(6)-5+0.125*iii)^2+(x(9)-5+0.125*iii)^2)/4); 
end

for iii=1:80;
    f3=f3+s3(iii)^2;
end

m3=(81*(f3+1))^0.5;

v=1/D*(-c3*z3-(z3^3*x(9)*m3^2)/(2*a3^2));
%%

if v>=b2;
    p=-D*b2;
elseif -b1<v && v<b2;
    p=-v*D;
else
    p=D*b1;
end

diary on
disp(t)


xdot=[    x(2)
          x(3)-0.01*x(2)-0.1*sin(x(1))+(x(1)^2*cos(x(2)*x(3)))*randn*0.01
          (v*D+p)-0.2*x(2)-0.02*x(3)    
          x(5)+k1*(x(1)-x(4))
          x(6)+k2*(x(1)-x(4))
          v*D+p+k3*(x(1)-x(4))
          (sig1-x(7))/w1
          (sig2-x(8))/w2
          rou*((z1^6*m1^2)/(2*a1^2)+(z2^6*m2^2)/(2*a2^2)+(z3^6*m3^2)/(2*a3^2)-kar*x(9))
    ];


end




 

   
